   EXECUTE IMMEDIATE 'DROP TABLE USER_RATINGS CASCADE CONSTRAINTS';
   EXECUTE IMMEDIATE 'DROP TABLE USER_FOLLOWERS CASCADE CONSTRAINTS';
   EXECUTE IMMEDIATE 'DROP TABLE PROPERTY_LIKES CASCADE CONSTRAINTS';
   EXECUTE IMMEDIATE 'DROP TABLE PROPERTY_IMAGES CASCADE CONSTRAINTS';
   EXECUTE IMMEDIATE 'DROP TABLE PROPERTIES CASCADE CONSTRAINTS';
   EXECUTE IMMEDIATE 'DROP TABLE USERS CASCADE CONSTRAINTS';
   EXECUTE IMMEDIATE 'DROP TABLE ORDERS CASCADE CONSTRAINTS';
